export enum HttpMethod {
  // Represents an HTTP GET request, used to retrieve data.
  GET = "GET",
  // Represents an HTTP POST request, used to submit data to be processed.
  POST = "POST",
  // Represents an HTTP PUT request, used to update or replace data.
  PUT = "PUT",
  // Represents an HTTP DELETE request, used to delete data.
  DELETE = "DELETE",
  // Represents an HTTP PATCH request, used to make partial updates to data.
  PATCH = "PATCH",
  // Represents an HTTP HEAD request, used to retrieve headers.
  HEAD = "HEAD",
  // Represents an HTTP OPTIONS request, used to describe the communication options.
  OPTIONS = "OPTIONS",
}
